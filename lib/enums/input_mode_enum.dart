/// Defines input modes for the input elements
/// in the editing modal.
///
/// `InputMode.search` refers to the text input that
/// allows the user to filter foods based on a key
/// word.
///
/// `InputMode.text` refers to the text input that
/// allows the user to set a name for a new food.
///
/// `InputMode.number` refers to the number input that
/// allows the user to set a price for a new food.
enum InputMode { search, text, number }
